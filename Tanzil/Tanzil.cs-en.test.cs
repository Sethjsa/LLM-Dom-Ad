Rozsuzuj tedy mezi nimi dle toho, co Bůh byl seslal a nenásleduj choutek jejich: měj se na pozoru, aby neodloudili tě od některých příkazů, seslaných ti Bohem.
Zajisté uvěřil jsem v Pána našeho: pročež slyšte mne!“
V tomto zákoně odvety jest ochrana života vašeho pro vás, kdož rozumní jste. Tak snad báti budete se Boha.
A když děti vaše dosáhnou mužnosti, nechť žádají o dovolení vstoupit, tak jako to žádali ti, kdož byli před nimi.
Bůh zabraňuje vám jen, aby přátelili jste se s těmi, kdož válčili proti vám v příčině náboženství a vyháněli vás z obydlí vašich a pomáhali ve vyhánění vás: a ten, kdo přátelí se s nimi, patřiti bude k nepravostným.
Bohu pak přináleží věci skryté nebes i země, a k němu vše navrací se: pročež uctívej jej a spoléhej naň: a Pán tvůj zajisté není lhostejným k tomu, co činíte.
A poslali jsme tě jen jako milosrdenství veškerenstvu.
Toto odměnou jejich bude za to, že ve znamení Naše nevěřili a říkali: Až staneme se kostmi a prachem, budeme vzkříšeni nějakým novým stvořením?
A já jsem si tě vyvolil, proto slyš, co vnuknuto ti bude!

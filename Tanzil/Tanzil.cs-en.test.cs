Požehnán budiž ten, jenž na nebi znamení zvěrokruhu stvořil a svítilnu tam umístil i měsíc osvětlený.
A jak hnusné bylo to, co dělali!
Kdo může vést ty, jimž Bůh dal zbloudit a kteří žádné pomocníky nemají?
Kdysi poslali jsme Mojžíše se znameními našimi a mocí zjevnou,
a věru jim sdělíme se znalostí vše, vždyť My jsme nebyli nepřítomni.
Přijdou-li k tobě, abys rozsoudil mezi nimi, buď vyřkni rozsudek svůj, neb vzdal se jich.
A věru jsou v tom znamení a My vskutku jsme byli těmi, kdož zkouškám podrobují.
A lid Noemův, když lháři nazvali proroky, potopili jsme a učinili jsme je lidstvu znamením: a přichystali jsme nepravostným trest bolestný.
aby jim objasnil to, o čem se hádali; a aby poznali ti, kdož nevěřili, že lháři byli.
